  <h1>Museo internazionale delle marionette Antonio Pasqualino</h1>
  <p>
    Il Museo internazionale delle marionette Antonio Pasqualino è stato istituito
    nel 1975 dall'Associazione per la conservazione delle tradizioni popolari.
    Fin dalla fondazione si è riferito costantemente ai criteri della museografia
    contemporanea per essere non tempio di un sapere per pochi, ma un luogo di
    comunicazione per una diffusa fruizione. Alle tradizionali attività
    museografiche vengono da sempre affiancati spettacoli dal vivo, realizzando
    uno degli esempi più felici di ricerca museografica sul teatro. La storia del
    Museo è indissolubilmente legata al suo fondatore, Antonio Pasqualino,
    scomparso nel 1995, medico chirurgo, insigne antropologo e cultore della
    storia e delle tradizioni popolari della sua terra natale, la Sicilia.
    Antonio Pasqualino dedicò la sua attività di ricerca ad una forma teatrale
    che, nella seconda metà del Novecento, sembrava ormai avviata verso un
    progressivo e inarrestabile declino: l'opera dei pupi, che narrava delle
    Crociate e dei Paladini di Francia, dei quali avevano scritto, fra gli altri,
    maestri della letteratura italiana, come Ludovico Ariosto, Matteo Maria
    Boiardo e Torquato Tasso.
  </p>
  <p>
    Antonio Pasqualino – insieme a un gruppo di intellettuali – fondò nel 1965
    l'Associazione per la conservazione delle tradizioni popolari e raccolse
    insieme alla moglie Janne Vibaek molte testimonianze: dai testi di scena ai
    pupi fino ai teatrini e gli arredi, salvandoli dalla distruzione e
    dall'oblio.
  </p>
  <p>
    Nel 1975 l'Associazione per la conservazione delle tradizioni popolari fondò
    il Museo internazionale delle marionette dove trovarono definitiva
    sistemazione i pupi e i diversi materiali raccolti nel corso degli anni. Tra
    questi, oggetti provenienti da numerosi Paesi europei e dell'Estremo Oriente
    come la Francia, la Spagna, la Thailandia, la Birmania, il Vietnam, e tanti
    altri. Da allora, è stato un susseguirsi di iniziative – come l'annuale
    Festival di Morgana che ospita artisti provenienti da tutto il mondo – e di
    scambi culturali con analoghe strutture nei cinque continenti. Nel 1995
    Antonio Pasqualino è venuto a mancare e al Museo è stato dato il suo nome.
  </p>
  <p>
    Il Museo delle marionette esprime la volontà di salvaguardare una forma di
    teatro prezioso della storia e della cultura siciliane, ma vuole essere anche
    l'occasione per ripensarle criticamente, per respingere il veleno del
    particolarismo locale, quel senso di insularità di cui Sciascia parlava come
    di un male oscuro. Il Museo attraverso la rappresentazione della leggenda di
    Carlomagno e dei suoi paladini testimonia un ideale di unità europea e
    soprattutto vuole inserire l'opera dei pupi nell'ampio quadro internazionale
    del teatro di animazione, per ristabilire attraverso la comparazione tra
    somiglianze e differenze un legame di solidarietà fra tutte le culture umane.
  </p>
  <p>
    È evidente l'interdipendenza delle varie attività del Museo. La Rassegna
    dell'opera dei pupi è divenuta nel 1985 Festival di Morgana assumendo
    rilevanza internazionale. Il confronto con compagnie provenienti da altre
    tradizioni e scuole italiane e straniere ha ridato orgoglio e dignità
    professionale ai pupari siciliani e ha ridato nuova linfa alle loro attività.
    La cadenza annuale del Festival è stata anche l'occasione per acquisire
    oggetti nuovi e ha posto le basi per lo studio sistematico di tradizioni e
    pratiche teatrali extraeuropee. Del rapporto con le maggiori istituzioni
    culturali nazionali e internazionali rimane traccia nel fitto lavoro di
    mostre e progetti teatrali legati alla tradizione siciliana realizzati anche
    all'estero.
  </p>
  <p>
    Il progetto teatrale del Museo si è anche rivolto alla produzione di
    spettacoli innovativi e, oltre a stimolare la produzione di una nuova
    drammaturgia da parte di scrittori e musicisti contemporanei (Italo Calvino,
    Francesco Pennisi, Luciano Berio) ha affidato la creazione di scenografie e
    di marionette a pittori o artisti visivi (Renato Guttuso, Tadeus Kantor,
    Enrico Baj), acquisendo così materiali di grande interesse.
  </p>
  <p>
    Al Museo è annessa la Biblioteca "Giuseppe Leggio" che raccoglie oltre
    11.000 pubblicazioni su pupi e marionette e sulle tradizioni popolari.
    Particolarmente preziose sono le collezioni di copioni manoscritti
    dell'Ottocento e del principio del Novecento, e di dispense cavalleresche
    pubblicate tra la fine del XIX e l'inizio del XX secolo.
  </p>
  <p>
    Il Museo possiede anche un ricco archivio fotografico, una videoteca e una
    nastroteca con registrazioni di spettacoli di teatro di figura di diversi
    Paesi.
  </p>
  <p>
    Particolare attenzione è rivolta all'attività didattica. Per le scolaresche
    sono previste visite guidate corredate da supporti video, mentre per gli
    operatori scolastici si organizzano corsi teorico-pratici sulle varie
    tecniche del teatro d'animazione. Su richiesta è anche possibile organizzare
    spettacoli di opera dei pupi.
  </p>
  <h3>La collezione</h3>
  <p>
    Il Museo internazionale delle marionette Antonio Pasqualino custodisce oltre
    5.000 pezzi, fra marionette, pupi, burattini, ombre, attrezzature sceniche e
    cartelloni provenienti da tutto il mondo. Tra questi, il Museo conserva la
    più vasta e completa collezione di pupi di tipo palermitano, catanese e
    napoletano, nonché esemplari dei "pupi" del Belgio e della Francia. Il
    nucleo fondativo della collezione è costituito da due mestieri di opera dei
    pupi siciliana, uno di tradizione palermitana, proveniente dalla storica
    famiglia Canino, e uno di tradizione catanese, proveniente dal teatro di
    Natale Meli.
  </p>
  <p>
    Oltre all'opera dei pupi, la collezione del Museo internazionale delle
    marionette Antonio Pasqualino conserva manufatti utilizzati in altre pratiche
    del teatro di figura italiano e straniero e, tra queste, anche opere di
    altre tradizioni che, come l'opera dei pupi, sono state dichiarate
    dall'UNESCO Capolavori del patrimonio orale e immateriale dell'umanità: il
    Ningyo Johruri Bunraku giapponese, il Wayang Kulit indonesiano, lo Sbek Thom
    cambogiano, il Namsadang Nori - Kkoktu-gaksi Norum coreano, il Karagöz
    turco e le Rūkada Nātya dello Sri Lanka.
  </p>
  <p>
    La collezione è inoltre arricchita da importanti opere d'arte contemporanea
    realizzate per tre spettacoli che tra gli anni Ottanta e Novanta furono
    prodotti dal Museo internazionale delle marionette: le scenografie di Renato
    Guttuso utilizzate nello spettacolo Foresta-radice-labirinto di Italo
    Calvino, regia di Roberto Andò (1987); le marionette e macchine sceniche
    realizzate dall'artista e regista polacco Tadeusz Kantor per lo spettacolo
    Macchina dell'amore e della morte (1987); e i pupazzi di Enrico Baj
    realizzati per lo spettacolo Le bleu-blanc-rouge et le Noir dell'Arc-en-terre
    di Massimo Schuster. Di recente acquisizione, le marionette da tavolo che
    Enrico Baj realizzò per altri due spettacoli di Massimo Schuster:
    Roncisvalle e Mahabharata.
  </p>
  <p>
    Oltre alla salvaguardia e conservazione di opere, il Museo internazionale
    delle marionette Antonio Pasqualino svolge una ricca e articolata attività di
    promozione del teatro animato e dell'opera dei pupi anche attraverso
    l'organizzazione di mostre in Italia e all'estero; particolare attenzione è
    rivolta altresì all'attività di produzione e didattica.
  </p>
  <p>
    A conferma del suo valore culturale, al Museo è stato assegnato nel 2001 il
    prestigioso premio antropologico "Costantino Nigra" per la sezione musei.
  </p>
  <p>
    Inoltre, il 27 ottobre 2017 a Milano, presso Palazzo Brera, nella sala della
    Biblioteca Nazionale Braidense, il Museo internazionale delle marionette
    Antonio Pasqualino è stato proclamato vincitore del Premio Icom – Italia,
    "Museo dell'anno 2017".
  </p>
