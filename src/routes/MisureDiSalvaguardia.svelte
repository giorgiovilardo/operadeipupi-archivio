  <h3>Il contesto</h3>
  <p>
    Nel 2013 l'Associazione per la conservazione delle tradizioni popolari, in
    qualità di organo competente in riferimento all'elemento Unesco dell'opera
    dei pupi siciliani, interpellato dal MiBACT ai fini dell'invio del Report
    periodico dell'Italia sull'attuazione della Convenzione Unesco del 2003, ha
    attestato la presenza di diverse famiglie di pupari attive sul territorio
    regionale rilevando una rinnovata vivacità dell'opera dei pupi, colpita
    negli anni Cinquanta-Sessanta da una profonda crisi che l'aveva fortemente
    messa a rischio di estinzione. La ripresa delle attività da parte di alcune
    delle famiglie storiche di pupari siciliani e il sorgere di qualche nuova
    compagnia, benché denoti un trend positivo, non può tuttavia mettere in
    ombra le criticità che ancora persistono in conseguenza della mancanza di
    specifiche linee guida e di un efficace ed aggiornato modello di
    salvaguardia di questa importante risorsa culturale. Non a caso l'opera dei
    pupi rientra tra le priorità di intervento delle istituzioni locali ed è
    oggetto di finanziamento specifico secondo la legge della Regione siciliana
    n. 25/2077, art. 11 e ss. mm. Benché molto sia stato fatto
    dall'Associazione per la conservazione delle tradizioni popolari dal 1965 ad
    oggi, nell'ambito di un ampio quadro programmatico di tutela,
    conservazione, promozione e valorizzazione dell'elemento, l'assenza di un
    Piano delle Misure di salvaguardia dell'opera dei pupi condiviso e
    partecipato dall'intera comunità si ripercuote inevitabilmente su questa
    pratica rappresentativa dell'identità locale e sulle sue future prospettive
    di sopravvivenza e sviluppo. Nonostante l'indubbio valore culturale
    dell'opera dei pupi, appare ancora lacunoso e fragile il nesso tra le
    operazioni di salvaguardia e valorizzazione messe in atto e
    l'individuazione e applicazione di metodi e strumenti di tipo legale,
    amministrativo, finanziario e tecnico finalizzati all'avvio e
    implementazione di uno sviluppo reale capace di coinvolgere le risorse
    locali e questa particolare espressione del patrimonio immateriale in una
    maglia di azioni integrate di tutela, conservazione e valorizzazione. Ancora
    molto c'è da fare in riferimento alla pianificazione anche economica
    nell'ambito di una politica che sia in grado di individuare corretti
    indirizzi di conoscenza, conservazione e valorizzazione orientati verso lo
    sviluppo delle risorse distintive del territorio. Conseguentemente, allo
    stato attuale, le famiglie e compagnie di pupari operano con fatica nelle
    aree di riferimento. Non tutte dispongono di teatri stabili e di adeguati
    spazi per la conservazione ed eventuale fruizione delle collezioni storiche
    della famiglia. Le loro possibilità di sopravvivenza sono legate alla
    capacità individuali di reperimento fondi e vendita di spettacoli,
    laboratori e manufatti in un contesto di forte competizione sia con le altre
    famiglie di pupari, sia con altre forme di teatro e intrattenimento. In un
    tale contesto di forte competizione, il processo di trasmissione del
    patrimonio orale di cui sono depositari i maestri pupari rischia di ridursi
    ad un'attività di semplice divulgazione che, benché sia necessaria per
    formare e informare il vasto pubblico, non può in alcun modo sostituirsi ad
    una formazione approfondita ed esaustiva in grado di generare nuove
    generazioni di marionettisti e artigiani capaci di coniugare la conoscenza e
    abile maestranza dei codici tradizionali con la legittima esigenza di
    innovazione e "ri-creazione" (per usare un termine della Convenzione Unesco)
    del patrimonio immateriale. I benefici di un contesto generalmente attrattivo
    sul piano turistico si coniugano d'altronde con ulteriori problematicità sia
    in termini di adeguata valorizzazione e sfruttamento delle risorse
    culturali, sia in termini di irregolare flusso di utenti e risorse tra alta
    e bassa stagione.
  </p>

  <h3>Gli obiettivi</h3>
  <p>
    Il Piano delle Misure di salvaguardia dell'Opera dei pupi siciliani ha
    inteso in primo luogo identificare adeguate misure di salvaguardia
    dell'Elemento, articolandole in un piano coerente che delinea una governance
    partecipata multilivello volta allo sviluppo sostenibile, in coerenza con le
    Direttive operative della Convenzione per la salvaguardia del patrimonio
    culturale immateriale. Obiettivi specifici perseguiti dal progetto: creare un
    dialogo tra le comunità dei portatori, ovvero le famiglie/compagnie
    dell'Opera dei pupi siciliani, gli artigiani e gli stakeholders in ambito
    istituzionale, accademico, turistico e dei servizi culturali la cui
    partecipazione attiva nelle azioni di progetto in qualità di membri della
    comunità di eredità sarà funzionale all'accrescimento della visibilità
    dell'Elemento a livello nazionale e internazionale; promuovere la creatività
    e la diversità culturale anche a livello nazionale e internazionale in
    quanto il Piano è stato incentrato, dandole rilievo internazionale, su una
    pratica performativa tradizionale a forte carattere comunitario ancora oggi
    vivace, creativa e creatrice di nuovi sensi e connessioni. Le radici
    dell'Opera dei pupi, che si rifanno ai miti fondativi dell'Europa, si
    sviluppano nella contemporaneità, offrendo una risposta a quei dilaganti
    processi di globalizzazione che determinano l'impoverimento,
    l'appiattimento e la standardizzazione delle espressioni culturali locali.
    La ricerca, il Piano di Salvaguardia e il portale online hanno tra gli
    obiettivi prioritari quello di sostenere e rafforzare tale risposta
    contribuendo alla conoscenza, diffusione e trasmissione dell'Opera dei pupi
    e soffermandosi sulle sue molteplici declinazioni contemporanee; coinvolgere
    direttamente la comunità e favorire la sua partecipazione attiva alle
    iniziative; favorire il dialogo e il confronto tra i diversi gruppi di
    pratica e tra essi e i ricercatori e gli studiosi in ambito antropologico,
    teatrale, artistico e letterario; interpellare i fruitori reali e
    potenziali dell'Opera dei pupi e gli stakeholders per il supporto e
    coinvolgimento nella redazione di un Piano delle Misure di salvaguardia
    efficace e duraturo.
  </p>

