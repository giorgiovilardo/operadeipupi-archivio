  <h3 id="associazione">
    Associazione per la conservazione delle tradizioni popolari
  </h3>
  <p>
    L'Associazione per la conservazione delle tradizioni popolari (1965) è il
    Soggetto referente della "Rete italiana di organismi per la tutela,
    promozione e valorizzazione dell'opera dei pupi" che riunisce tutte le
    famiglie e compagnie di pupari siciliani. Formalmente costituita nel 2018
    attraverso la sottoscrizione di un formale Atto di di intesa, in occasione
    della presentazione del progetto la Rete è stata riconosciuta dal Ministero
    dei Beni e delle Attività Culturali e del Turismo quale organismo
    territoriale competente in materia di salvaguardia e rappresentativo della
    tradizione dell'opera dei pupi siciliana. L'impegno del Soggetto referente
    nell'ambito dell'PCI in riferimento all'opera dei pupi si è concretizzato
    nel sostegno e redazione della proposta di candidatura dell'opera dei pupi
    presso l'UNESCO che ha portato alla sua proclamazione come "Capolavoro del
    patrimonio orale e immateriale dell'umanità". In virtù delle comprovate
    competenze nel campo della ricerca e dello studio del patrimonio
    immateriale, nel 2014 l'Associazione e stata accreditata come ONG
    consulente del Comitato Intergovernativo UNESCO dell'ICH. Nel 2019, il
    Comitato ha rinnovato l'accreditamento dell'Associazione per la
    conservazione delle tradizioni popolari quale suo consulente, testimoniando
    il raggiungimento di alcuni importanti obiettivi dal 2014 ad oggi in piena
    aderenza a quanto stabilito dalla Convenzione Unesco per la Salvaguardia del
    patrimonio culturale immateriale del 2003. La conferma dell'accreditamento
    dell'Associazione riconosce a livello internazionale l'efficacia di un
    progetto di salvaguardia partecipe e condiviso dalla comunità di eredità,
    fondato su expertise e metodologie aggiornate perfettamente allineate con le
    attuali politiche dell'Unesco. Nel 2015, il soggetto referente è stato
    iscritto all'Anagrafe Nazionale delle Ricerche, del MIUR (codice
    identificativo 61993JYI). Le misure e strategie fin qui adottate sono state
    premiate nel 2017 dall'ICOM che ha assegnato al Museo internazionale delle
    marionette Antonio Pasqualino il premio ICOM-Italia come "Museo dell'anno".
    Nel premiare il Museo Pasqualino, ICOM-Italia ha così riconosciuto il
    valore di un intero ambito artistico e teatrale, quello del teatro di figura
    e dell'opera dei pupi, tradizionalmente considerato come minore. Nel 2018,
    il Museo ha accolto la conferenza internazionale del progetto europeo IMP
    (Intangible Cultural Heritage and Museum Project).
  </p>

