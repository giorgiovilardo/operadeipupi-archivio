  <h1>
    Il Piano delle Misure di salvaguardia dell'opera dei pupi siciliana
  </h1>
  <p>
    Il Piano delle Misure di salvaguardia dell'Opera dei pupi siciliani è stato
    redatto, primo in Italia, nel 2020 nell'ambito di un progetto promosso
    dall'Associazione per la conservazione delle tradizioni popolari di Palermo
    quale Soggetto referente della "Rete italiana di organismi per la tutela,
    promozione e valorizzazione dell'Opera dei pupi: #OPERADEIPUPI.IT#", con il
    finanziamento del Ministero dei Beni Culturali e del Turismo, Legge 20
    febbraio 2006, n. 77 "Misure speciali di tutela e fruizione dei siti e
    degli elementi di interesse culturale, paesaggistico e ambientale, inseriti
    nella 'lista del patrimonio mondiale', posti sotto tutela dell'UNESCO" e con
    il supporto dell'indirizzo tecnico-scientifico dell'Ufficio UNESCO del
    Segretariato Generale del MiBACT. Il Piano individua le priorità di
    intervento identifificando le strategie e le modalità di salvaguardia, che
    comprendono l'identifificazione partecipativa, la ricerca e documentazione,
    la trasmissione attraverso l'educazione formale e non formale, la protezione
    giuridica, la valorizzazione e promozione dell'Elemento e ne prefigura le
    modalità attuative. L'obiettivo generale del Piano è favorire e attuare un
    progetto di governance partecipata multilivello coerente con le
    <a
      href="http://www.unesco.it/it/ItaliaNellUnesco/Detail/189"
      target="_blank"
      rel="noopener noreferrer"
    >Direttive operative della Convenzione Unesco per la Salvaguardia del
      patrimonio culturale immateriale del 2003</a>,
    fondato sul coinvolgimento dei diversi stakeholders per dare il necessario
    supporto ai detentori e praticanti dell'Opera dei pupi. Ai fini della
    redazione del Piano, è stata organizzata una campagna di ricerca sullo stato
    dell'elemento, base scientifica imprescindibile per valutare lo stato
    dell'arte dell'opera dei pupi, individuandone le problematiche culturali,
    artistiche, storiche, ambientali, scientifiche e tecniche. Il metodo di
    ricerca partecipativo ha permesso di coinvolgere attivamente quelle
    "comunità gruppi e individui" (art.2 CICH) che la
    <a
      href="http://www.unesco.it/it/ItaliaNellUnesco/Detail/189"
      target="_blank"
      rel="noopener noreferrer"
    >Convenzione di Faro definisce</a>
    comunità di eredità, ovvero «insiemi di persone che attribuiscono valore a
    degli aspetti specifici dell'eredità culturale, che desiderano, nell'ambito
    di un'azione pubblica, sostenere e trasmettere alle generazioni future».
  </p>
  <p>
    Il progetto sul "Piano delle Misure di salvaguardia dell'opera dei pupi
    siciliani" si inserisce in un più ampio quadro programmatico di tutela,
    conservazione, promozione e valorizzazione dell'opera dei pupi siciliana
    attuato a partire dagli anni Sessanta dal soggetto referente, l'<a
      href="#associazione"
    >Associazione per la conservazione delle tradizioni popolari.</a>
    Istituita negli anni della crisi dell'opera dei pupi, l'Associazione ha
    avviato un progetto, ancora in corso e fortemente partecipato, di rilancio e
    rivitalizzazione dell'opera dei pupi, collaborando nel tempo con tutte le
    famiglie/compagnie di pupari e artigiani nell'ambito dei diversi progetti e
    sostenendo economicamente la loro attività anche attraverso la commissione
    di spettacoli tradizionali e di innovazione. Coerentemente con il
    <em>know how</em> dell'ente responsabile, il progetto si fonda su un
    approccio interculturale e multidisciplinare e sull'intreccio di attività di
    varia natura.
  </p>
