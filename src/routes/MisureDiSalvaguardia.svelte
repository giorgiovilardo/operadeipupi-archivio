  <h1>
    Piano strategico particolareggiato delle misure di salvaguardia del teatro
    dell'Opera dei pupi siciliani
  </h1>
  <p>
    Il Piano Strategico Particolareggiato delle Misure di Salvaguardia del
    teatro dell'Opera dei pupi siciliani (PSP) è un documento programmatico
    redatto nel corso del 2023 nell'ambito del progetto "L'opera dei pupi
    siciliani: pianificazione strategica, trasmissione, valorizzazione",
    promosso dall'Associazione per la conservazione delle tradizioni popolari -
    ETS di Palermo in qualità di soggetto referente della "Rete italiana di
    organismi per la tutela, promozione e valorizzazione dell'Opera dei pupi:
    #OPERADEIPUPI.IT#" e organismo competente per la salvaguardia del patrimonio
    culturale immateriale del territorio, con particolare riferimento all'Opera
    dei pupi siciliani. Il Piano è stato finanziato del Ministero della Cultura,
    Legge 20 febbraio 2006, n. 77 "Misure speciali di tutela e fruizione dei
    Siti e degli elementi di interesse culturale, paesaggistico e ambientale,
    inseriti nella 'lista del patrimonio mondiale', posti sotto tutela
    dell'Unesco". Coerentemente con la Convenzione Unesco per la Salvaguardia
    del patrimonio culturale immateriale del 2003, con le connesse Direttive
    Operative e con la Convenzione quadro del Consiglio d'Europa sul valore del
    patrimonio culturale per la società di Faro del 2005, il Piano Strategico
    Particolareggiato delle Misure di salvaguardia dell'Opera dei pupi siciliani
    individua specifiche soluzioni per la salvaguardia e la governance
    sostenibile dell'Opera dei pupi, inserito nella lista Unesco dei Capolavori
    del patrimonio orale e immateriale dell'umanità nel 2001. Partendo dai
    bisogni della comunità patrimoniale e del territorio e dai contesti di
    pratica, esso pianifica le azioni e le attività da intraprendere e avviare
    entro gennaio 2030 con l'obiettivo di attuare le misure individuate di
    concerto con la comunità patrimoniale e descritte dettagliatamente nel Piano
    delle Misure di salvaguardia dell'Opera dei pupi redatto nel 2020; programma
    gli interventi necessari per raggiungere gli obiettivi generali e specifici
    individuati e aggiornati nel 2023, da verificare con una precisa definizione
    di indicatori che intrecciano quelli dell'Overall Results Framework per la
    Convenzione Unesco del 2003.
