  <h1>Le normative UNESCO</h1>
  <p>
    A maggio 2001 l'UNESCO proclama per la prima volta 19 capolavori del
    Patrimonio orale e immateriale dell'umanità tra cui l'opera dei pupi
    siciliana. Le pratiche culturali tradizionali che ottengono tale
    riconoscimento vengono selezionate per il loro valore artistico, storico e
    antropologico in quanto espressioni viventi dell'identità culturale delle
    rispettive comunità.
  </p>
  <p>
    Nel 2003, in occasione della 32° Sessione, la Conferenza generale
    dell'UNESCO adotta la
    <a href="https://ich.unesco.org/en/convention" target="_blank" rel="noopener noreferrer">Convenzione</a>
    per la salvaguardia del patrimonio culturale immateriale che costituisce una
    pietra miliare nell'evoluzione delle politiche internazionali per la
    promozione della diversità culturale.
  </p>
  <p>
    Attraverso la Convenzione per la prima volta la comunità internazionale
    riconosce l'importanza del patrimonio culturale immateriale e sottolinea la
    necessità di salvaguardarlo attraverso l'adozione di uno specifico quadro
    giuridico e programmatico.
  </p>
  <p>
    L'articolo 2 della Convenzione UNESCO per la Salvaguardia del Patrimonio
    Culturale Immateriale del 2003, definisce così il patrimonio culturale
    immateriale:<br /><br />
    «Si intendono per "patrimonio culturale immateriale" le pratiche, le
    rappresentazioni, le espressioni, le conoscenze e i saperi – così come gli
    strumenti, gli oggetti, i manufatti e gli spazi culturali ad essi associati
    – che le comunità, i gruppi e, in alcuni casi, gli individui riconoscono
    come facenti parte del loro patrimonio culturale. Tale patrimonio culturale
    immateriale, trasmesso di generazione in generazione, è costantemente
    ricreato dalle comunità e dai gruppi interessati in conformità al loro
    ambiente, alla loro interazione con la natura e alla loro storia, e fornisce
    loro un senso di identità e continuità, promuovendo così il rispetto per la
    diversità culturale e la creatività umana».
  </p>
  <p>
    Tale definizione costituisce il risultato di uno scambio di lunga data in
    ambito intergovernativo sul valore, sulla natura e sulle diverse concezioni
    del patrimonio culturale e sulle sue diverse manifestazioni in rapporto alle
    identità dei popoli. Un iter avviato in seno all'Unesco a partire dal
    Dopoguerra nell'ottica di promuovere la pace tra i popoli attraverso
    l'istruzione, la scienza e la cultura e di avviare forme di cooperazione
    internazionale nel campo delle arti nel rispetto della diversità culturale.
  </p>
  <p>
    Sulla scia di questo lungo percorso, il nuovo millennio ha visto avviare
    attraverso le proclamazioni e la Convenzione del 2003 un'imponente campagna
    di sensibilizzazione mondiale sul valore del patrimonio culturale
    immateriale nelle sue molteplici forme e sulla necessità di individuare e
    attuare adeguate misure di salvaguardia. L'importanza di tale patrimonio,
    nella sua fragilità, risiede nella ricchezza di conoscenze e abilità che
    viene trasmessa tra generazioni attraverso esso. Il valore sociale ed
    economico di questa trasmissione di conoscenze è rilevante per le minoranze
    e per i gruppi sociali all'interno di uno Paese, sia esso in via di
    sviluppo o non.
  </p>
  <p>
    Parallelamente, a Palermo, sin dagli Sessanta, l'Associazione per la
    conservazione delle tradizioni popolari – fondatore del Museo delle
    marionette – opera in questa stessa direzione. Ricercatori e operatori sono
    infatti consapevoli dell'importanza di salvaguardare e valorizzare non
    soltanto i manufatti – tangibili – ma, come nel caso dell'opera dei pupi,
    anche lo spettacolo e tutte le pratiche correlate. È così che alla raccolta
    degli oggetti si affianca la registrazione di spettacoli e interviste sulle
    tecniche di costruzione e sulla manovra dei pupi che avvengono ancora
    secondo regole e codici trasmessi oralmente. Su queste basi, si è potuta
    presentare la candidatura dei pupi siciliani alla prima selezione
    dell'UNESCO, nel 2001, con un'esperienza quarantennale alle spalle.
  </p>
  <p>
    La Convenzione Unesco per la salvaguardia del patrimonio culturale e
    immateriale del 2003 è stata ratificata dall'Italia il 30 ottobre 2007.
    Tuttavia bisognerà attendere il 2017 affinché il Ministero dei Beni e delle
    Attività Culturali e del Turismo modifichi la
    <a
      href="https://www.beniculturali.it/mibac/export/MiBAC/sito-MiBAC/Contenuti/MibacUnif/Comunicati/visualizza_asset.html_1755175100.html"
      target="_blank"
      rel="noopener noreferrer"
    >Legge n. 77/2006</a>
    includendo nelle misure speciali di tutela e fruizione non soltanto i siti
    ma anche gli elementi italiani di interesse culturale, paesaggistico e
    ambientale inseriti nella "lista del patrimonio mondiale", posti sotto la
    tutela dell' UNESCO. D'altronde, al livello locale, la normativa regionale
    presenta ancora uno squilibrio tra le misure adottate.
